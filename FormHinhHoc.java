    public void napDuLieuVaoBang(ArrayList <HinhTron>x){
        dtm.setRowCount(0);
        int dem=0;
        for(HinhTron y:x){
            if(y instanceof HinhChuNhat){
                dtm.addRow(new Object[]{dem,y.getName(),((HinhChuNhat) y).getCanh2(),((HinhChuNhat) y).getCanh1(),"",y.TinhDienTich()});
            }else if (y instanceof HiinhHopChuNhat) {
                dtm.addRow(new Object[]{dem,y.getName(),((HiinhHopChuNhat) y).getCanh1(),((HiinhHopChuNhat) y).getCanh2(),((HiinhHopChuNhat) y).getChieucao(),y.TinhDienTich()});
            }else{
                dtm.addRow(new Object[]{dem,y.getName(),y.getBanKinh(),"","",y.TinhDienTich()});
            }
            dem++;
        }
    }
