    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        HinhTron y = null;
        double a,b,c;
        if(txtA.getText()=="" ||txtB.getText()==""||txtC.getText()==""){
            JOptionPane.showMessageDialog(null,"VUI LÒNG NHẬP DỮ LIỆU");
        }else{
            if(rbtHT.isSelected()){
                a= Double.parseDouble(txtA.getText());
                y = new HinhTron(a);
            }else if (rbtHCN.isSelected()){
                a= Double.parseDouble(txtA.getText());
                b= Double.parseDouble(txtB.getText());
                y = new HinhChuNhat(a,b);
            }else{
                a= Double.parseDouble(txtA.getText());
                b= Double.parseDouble(txtB.getText());
                c= Double.parseDouble(txtC.getText());
                y=new HiinhHopChuNhat(a,b,c);
            }
        }
        if(x.themMoi(y)==false){
           JOptionPane.showMessageDialog(null, "HÌNH ĐÃ BỊ TRÙNG");
        }else{
            JOptionPane.showMessageDialog(null,y);
            napDuLieuVaoBang(x.getList());
        }
    }//GEN-LAST:event_btnThemActionPerformed

